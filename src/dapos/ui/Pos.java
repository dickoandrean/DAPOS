    public void checkdaily() throws ParseException{
    SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
    String now = f.format( new Date());
    Date tgl = f.parse(now);
        Connection con;
        Statement stat;
        ResultSet rs;
        String sql ;
        Koneksi db = new Koneksi();
        db.connect();
        con = (Connection) db.KoneksiDatabase;
        try{
        sql = "SELECT * FROM `daily_procedures` WHERE id=(SELECT MAX(id) FROM `daily_procedures`)";
        rs=db.stm.executeQuery(sql);
        if(rs.next()){
            if( rs.getString("closing")==null){
                if(rs.getDate("opening").before(tgl)){
                    JOptionPane.showMessageDialog(this, "Opening Day Procedure pada tanggal "+rs.getDate("opening")+". Sekarang tanggal "+f.format(tgl)+". Silahkan lakukan End Of Day Procedure.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Silahkan Lakukan Opening Day Procedures terlebih dahulu");
            }
        }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Silahkan Lakukan Opening Day Procedures terlebih dahulu");
            }
    }
    
