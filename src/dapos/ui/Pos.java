    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        Connection con;
        Statement stat;
        ResultSet rs;
        String user;
        user = username.getText();
        String pw;
        pw = password.getText();
        String sql;
        Koneksi db = new Koneksi();
        db.connect();
        con = (Connection) db.KoneksiDatabase;

        try{
            sql = "Select * from employee where username = '"+user+"' and password = '"+pw+"'" ;
            rs = db.stm.executeQuery(sql);
            if(rs.next()){
                if(user.equals(rs.getString("username")) && pw.equals(rs.getString("password"))){
                    UserSession.setU_id(rs.getInt("id"));
                    UserSession.setU_user(rs.getString("username"));
                    UserSession.setU_name(rs.getString("name"));
                    UserSession.setU_security(rs.getInt("security"));
                    this.dispose();
                    new Application().setVisible(true);
                    try {
                            checkdaily();
                        } catch (ParseException ex) {
                            Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
                        }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username dan Password Salah");
            }
        } catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_loginButtonActionPerformed

